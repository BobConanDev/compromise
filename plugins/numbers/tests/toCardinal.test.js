const test = require('tape')
const nlp = require('./_lib')
//tests 'borrowed' from Travis Savo's lib üíù https://github.com/TSavo/english2number-js
const cardinal = function (str) {
  return nlp(str).values().json(0).number
}

test('parse numbers', function (t) {
  t.equal(cardinal('0'), 0, '0')
  t.equal(cardinal('1'), 1, '1')
  t.equal(cardinal('2'), 2, '2')
  t.equal(cardinal('3'), 3, '3')
  t.equal(cardinal('4'), 4, '4')
  t.equal(cardinal('5'), 5, '5')
  t.equal(cardinal('6'), 6, '6')
  t.equal(cardinal('7'), 7, '7')
  t.equal(cardinal('8'), 8, '8')
  t.equal(cardinal('9'), 9, '9')
  t.equal(cardinal('10'), 10, '10')
  t.equal(cardinal('20'), 20, '20')
  t.equal(cardinal('30'), 30, '30')
  t.equal(cardinal('50'), 50, '50')
  t.equal(cardinal('90'), 90, '90')
  t.equal(cardinal('100'), 100, '100')
  t.end()
})
